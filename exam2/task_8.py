# 8**) Шахматный король ходит по горизонтали, вертикали и диагонали,
#
#  но только на 1 клетку. Даны две различные клетки шахматной доски,
#
# определите, может ли король попасть с первой клетки на вторую одним ходом.

def is_valid_king_move(cell1, cell2):
    """
    Определяет, может ли король попасть с первой клетки на вторую одним ходом.

    Args:
        cell1 (tuple): Координаты первой клетки (строка, столбец)
        cell2 (tuple): Координаты второй клетки (строка, столбец)

    Returns:
        bool: True, если король может попасть с первой клетки на вторую одним ходом, иначе False
    """

    # Извлекаем координаты клеток
    row1, col1 = cell1
    row2, col2 = cell2

    # Проверяем, что клетки разные
    if cell1 == cell2:
        return False

    # Король может ходить только на одну клетку в любом направлении
    return abs(row1 - row2) <= 1 and abs(col1 - col2) <= 1
def is_valid_knight_move(cell1, cell2):
    """
    Определяет, может ли конь попасть с первой клетки на вторую одним ходом.

    Args:
        cell1 (tuple): Координаты первой клетки (строка, столбец)
        cell2 (tuple): Координаты второй клетки (строка, столбец)

    Returns:
        bool: True, если конь может попасть с первой клетки на вторую одним ходом, иначе False
    """

    # Извлекаем координаты клеток
    row1, col1 = cell1
    row2, col2 = cell2

    # Проверяем, что клетки разные
    if cell1 == cell2:
        return False

    # Конь может ходить буквой "Г" - на две клетки в одном направлении и на одну клетку в перпендикулярном направлении
    return (abs(row1 - row2) == 2 and abs(col1 - col2) == 1) or (abs(row1 - row2) == 1 and abs(col1 - col2) == 2)
def is_valid_rook_move(cell1, cell2):
    """
    Определяет, может ли ладья попасть с первой клетки на вторую одним ходом.

    Args:
        cell1 (tuple): Координаты первой клетки (строка, столбец)
        cell2 (tuple): Координаты второй клетки (строка, столбец)

    Returns:
        bool: True, если ладья может попасть с первой клетки на вторую одним ходом, иначе False
    """

    # Извлекаем координаты клеток
    row1, col1 = cell1
    row2, col2 = cell2

    # Проверяем, что клетки разные
    if cell1 == cell2:
        return False

    # Ладья может ходить только по горизонтали или вертикали
    return row1 == row2 or col1 == col2
def is_valid_queen_move(cell1, cell2):
    """
    Определяет, может ли ферзь попасть с первой клетки на вторую одним ходом.

    Args:
        cell1 (tuple): Координаты первой клетки (строка, столбец)
        cell2 (tuple): Координаты второй клетки (строка, столбец)

    Returns:
        bool: True, если ферзь может попасть с первой клетки на вторую одним ходом, иначе False
    """

    # Извлекаем координаты клеток
    row1, col1 = cell1
    row2, col2 = cell2

    # Проверяем, что клетки разные
    if cell1 == cell2:
        return False

    # Ферзь может ходить как ладья или как слон
    return is_valid_rook_move(cell1, cell2) or is_valid_bishop_move(cell1, cell2)
def is_valid_bishop_move(cell1, cell2):
    """
    Определяет, может ли слон попасть с первой клетки на вторую одним ходом.

    Args:
        cell1 (tuple): Координаты первой клетки (строка, столбец)
        cell2 (tuple): Координаты второй клетки (строка, столбец)

    Returns:
        bool: True, если слон может попасть с первой клетки на вторую одним ходом, иначе False
    """

    # Извлекаем координаты клеток
    row1, col1 = cell1
    row2, col2 = cell2

    # Проверяем, что клетки разные
    if cell1 == cell2:
        return False

    # Слон может ходить только по диагоналям
    return abs(row1 - row2) == abs(col1 - col2)
# Пример 1: Король
cell1 = (4, 4)
cell2 = (5, 5)
print(is_valid_king_move(cell1, cell2))  # True

# Пример 2: Конь
cell1 = (1, 2)
cell2 = (3, 4)
print(is_valid_knight_move(cell1, cell2))  # True

# Пример 3: Ладья
cell1 = (2, 3)
cell2 = (2, 7)
print(is_valid_rook_move(cell1, cell2))  # True

# Пример 4: Ферзь
cell1 = (1, 1)
cell2 = (7, 7)
print(is_valid_queen_move(cell1, cell2))  # True

# Пример 5: Слон
cell1 = (3, 2)
cell2 = (7, 6)
print(is_valid_bishop_move(cell1, cell2))  # True



